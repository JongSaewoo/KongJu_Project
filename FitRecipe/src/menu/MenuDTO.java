package menu;

public class MenuDTO {
	int menuNum ;
	String userID;
	String menuName;
	String menuRecipe ;
	int menuPrice;
	int menuAvailable;
	String menuProfile;
	String menuRealProfile;
	public int getMenuNum() {
		return menuNum;
	}
	public void setMenuNum(int menuNum) {
		this.menuNum = menuNum;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuRecipe() {
		return menuRecipe;
	}
	public void setMenuRecipe(String menuRecipe) {
		this.menuRecipe = menuRecipe;
	}
	public int getMenuPrice() {
		return menuPrice;
	}
	public void setMenuPrice(int menuPrice) {
		this.menuPrice = menuPrice;
	}
	public int getMenuAvailable() {
		return menuAvailable;
	}
	public void setMenuAvailable(int menuAvailable) {
		this.menuAvailable = menuAvailable;
	}
	public String getMenuProfile() {
		return menuProfile;
	}
	public void setMenuProfile(String menuProfile) {
		this.menuProfile = menuProfile;
	}
	public String getMenuRealProfile() {
		return menuRealProfile;
	}
	public void setMenuRealProfile(String menuRealProfile) {
		this.menuRealProfile = menuRealProfile;
	}
	
	
	
}
